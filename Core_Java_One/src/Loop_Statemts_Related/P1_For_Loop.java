package Loop_Statemts_Related;

 public class P1_For_Loop 
 {
	 
	    public static void main(String args[]) 
	    { 
	        String array[] = {"Ron", "Harry", "Hermoine"}; 
	        
	        for (int i = 0; i < array.length; i++) 
	        { 
	            System.out.println(array[i]); 
	        } 
	        
	    } 
	} 


