package Arrays;

public class P1_Char_Array_Example 
{
	    public static void main(String[] args) 
	    {
	    	  // Create an array of four chars.
	        char[] values = new char[4];
	        values[0] = 'j';
	        values[1] = 'a';
	        values[2] = 'v';
	        values[3] = 'a';

	        // Loop over array with for-loop.
	        for (char value : values) {
	            System.out.println(value);
	        }
	    }
	}

